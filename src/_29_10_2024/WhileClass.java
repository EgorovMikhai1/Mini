package _29_10_2024;

public class WhileClass {

    public static void main(String[] args) {
        int x = 10;

        while (x > 0) {
            System.out.println("HI " + x);
            x--;
        }

        int y = 10;

        if (y < 5) {
            System.out.println("нет денег на бар");
        } else {
            System.out.println("Go!!");
        }
    }

}
